begin
	struct Scenario
	    demand::Float64
	    fuel_cost::Tuple{Float64, Float64, Float64}
	end
	
	function create_scenario(demand::Float64, fuel_cost::Tuple{Float64, Float64, Float64})
	    return Scenario(demand, fuel_cost)
	end
	
	scenario = create_scenario(850.0, (1.1, 1.0, 1.0))	
end

# ╔═╡ 00205ae3-cdb1-4c05-99c6-cadfea04c358
begin	
	function thermal_IO_function(h0, h1, h2, power)
	    return h0 + h1*power + h2*power^2
	end
	
	function solve_economic_dispatch(
    	generators::Vector,
    	scenario;
    	silent::Bool = false,
	)
    
		model = Model(Ipopt.Optimizer)
	    if silent
	        set_silent(model)
	    end
	    @operator(model, op_tcf, 4, thermal_IO_function)
	    N = length(generators)
	    @variable(model, generators[i].min <= g[i = 1:N] <= generators[i].max)
	    @objective(
	        model,
	        Min,
	        sum(scenario.fuel_cost[i] * op_tcf(generators[i].h0, generators[i].h1, generators[i].h2, g[i]) for i in 1:N),
	    )
	    c = @constraint(model, c1, sum(g[i] for i in 1:N) == scenario.demand)
	    optimize!(model)
		print(model)
		println(dual_status(model))
		println(shadow_price(c1))
	    return (
	        g = value.(g),
	        total_cost = objective_value(model),
			price = -shadow_price(c1),
			#solution_summary(model; verbose=true)
	    )
	end

	solution =
    	solve_economic_dispatch(generators, scenario)
end

